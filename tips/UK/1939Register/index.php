    <h1>A Guide to the 1939 Register</h1>
    <p>The 1939 Register is a very useful document and unlike a census.  It was carried out so that ID cards could be issued at the start of WW2 but was used to set up the NHS and updated until 1990. </p>
    <p>What information is available on the 1939 Register?  Each personâ€™s record contains:-</p>
    <ul>
        <li>Name</li>
        <li>Full Date of Birth</li>
        <li>Address</li>
        <li>Marital Status (update as women married)</li>
        <li>Occupation</li>
    </ul>
    <p>In most cases people are recorded as a household member however some are recorded as a member of an institution.   When recorded as a member of an institution, they will be classified by one of five letters:-</p>
    <ul>
        <li><samp class="inport">O</samp> = Officer</li>
        <li><samp class="inport">V</samp> = Visitor</li>
        <li><samp class="inport">S</samp> = Servant</li>
        <li><samp class="inport">P</samp> = Patient</li>
        <li><samp class="inport">I</samp> = Inmate</li>
    </ul>
    <p>The 1939 Register required people to describe in detail what they did so whereas on the census they would have been recorded as a warehouse man in this register they had to be more specific and so would be recorded as warehouseman, glass bottle works or colliery hewer, retired or Iron foundry labourer (heavy worker)  Also recorded if they were ARPs.</p>
    <p>Reasons you may not find your ancestor on the register are:-</p>
    <ul>
        <li>Their record is redacted because they are under 100 years old</li>
        <li>They died after 1990 and no death certificate has been submitted to get the record opened</li>
        <li>Resided in an area which was missed off the original when the Register was being compiled.</li>
        <li>Their entry has been mistranscribed</li>
    </ul>
    <p>The register is accessible online at <a href="https://www.findmypast.co.uk">www.findmypast.co.uk</a> and offline at the National Archives.</p>

    <p>On the FindmyPast website, additional information regarding the social history, e.g. photographs, newspaper articles, detailed maps etc.... accompany the register giving a true feel as to what life was like at the start of the war.</p>

