    <h1>Searching for a birth in England and Wales</h1>

    <p>Civil Registration began in 1837 however the onus was on the Registrar of each sub division of the Registry to               discover the births, marriage and deaths in his district and as you can imagine the major pitfall was that many went         unregistered.</p>
    <p>However, as a result of the Birth and Deaths Act of 1874 registration became compulsory by 1875 and the onus changed         from the Registrar to the parents to registering a birth within 42 days at the district or sub district office.              Failure to do so resulted in the parents being fined.</p>
    <p>It should also be remembered that there was no standardisation of spelling of names so although the birth may have           been registered the initial search fails due to the recorded spelling differing from that you have entered in the            search engine.</p>

    <p>Another reason for failure is that the name you are searching for is not the one the person was registered with e.g.         illegitimate births are registered in the mother’s name only but she may have married the father later or married            someone completely different and the child “adopted” the step father’s surname or recorded in census under the step          father’s name.  Adoption became a legalised procedure in 1927.</p>

    <p>Mother’s maiden names did not appear in the registrations until late 1911 however the General Registry Office of             England and Wales have created an index all the way back to 1837 which includes this information.  It can be accessed        for free on their website <a href="https://www.gro.gov.uk">www.gro.gov.uk</a>  It is also where you should order any          certificates from.    However, this index is very restrictive in its search options and so to narrow your options it         is sometimes better to use <a href="https://www.freebmd.org.uk">www.freebmd.org.uk</a>  where you can search by just        the first name or use wildcards * or a phonetic search.

    <p>Good luck with your research</p>

