    <h1>Don’t Get Ripped Off when ordering certificates for England and Wales</h1>
    <p>At some point we all need to order a Birth, Marriage or Death certificate for our English or Welsh ancestor and there are many sites offering this service however there is only one that you should use and that is the General Registry Offices (GRO) own site.   Depending on what service you opt for on non GRO sites depends on how much you are charged and these charges can vary from site to site however any certificate will be ordered from the GRO website or a local registry off where the event was first recorded.</p>
    <p>The GRO website now offers pdf versions of the certificates rather than a paper copy for certain certificates.  It is very much a work in progress and so far is proving very popular with genealogists and is definitely a step in the right direction.    In addition to this the website has been improved to offer an birth and death index search.  The birth index is extremely useful in that it lists mothers maiden names before 1911 when previously they were unavailable.   This can narrow searches for ancestors and reduce the need to buy a certificate.</p>
