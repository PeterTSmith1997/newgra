    <h1>UK Census</h1>
    <p>The UK census has been taken at ten yearly intervals since 1801 with the exception of 1841 however only those dating from 1841 have any significant value to the family historian. Earlier census returns between 1801 and 1830 was the responsibility of the Overseers of the Poor and the clergy and were more of a head count. The public however, are only given access to the records after 100 years for example, the 1901 census was published in 2002.  The exception was the 1911 census which following a lobby to the government was released early.    The 1939 register is not a census but does hold important information and does not fall under the census law.  It has therefore been published by FindmyPast.</p>
