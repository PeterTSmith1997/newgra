    <h1>Beginners guide to researching your UK ancestry - Tips on starting a tree</h1>
    <p>Decided to research your family’s history or tree and don’t know where to begin?</p>
    <p>Not to worry help is at hand.  Our Beginners Guide is here to help you take those first tentative steps on your own personal incredible            journey.</p>
    <h2>Step One:  </h2>
    <ol>
        <li>Start with what you know as fact, yourself, your parents and your grandparents.</li>
        <li>Ask family members, especially the older generations what they know.</li>
        <li>Listen to family stories but do not necessarily accept them as fact, discuss the old photos that have been put away and                             forgotten about and try to put names to the faces.
            <ol>
                <li>Record them on the back for future generations.</li>
            </ol>
        </li>
        <li>Get out the family documents and create a log book.  I find a lever arch file best because you can have a section for each                           family and add documents easily.  It is used to record the information you have, what you still need to find and any
            information that has been discarded e.g. birth registration in the index because you’ve bought the certificate and discovered                        it’s not the right one.  This is explained in more detail – <a href="../../general/loggingYourFindings/">Logging your Findings.</a></li>
    </ol>
    <h2>Step Two</h2>
    <p>Now we can move on to the second step.  Chart out the information you know, this can be done on a piece of paper or by using                         computer software.  There is a very good free download of family tree software at
        <a target="_blank" href="http://www.legacyfamilytree.com"> www.legacyfamilytree.com</a></p>
        <h2>Step Three:</h2>
        <p>Find evidence which is missing from the documentation you already have e.g. grandparents birth certificates, WW1 military records, 1939              register, census records etc.....  The indexes for birth, marriages and deaths can be viewed at <a target="_blank" href="http://www        .freebmd.org.uk">www.freebmd.org.uk</a> (which covers England and Wales.  For Scottish records you need <a href="http://www.scotlandspeople.gov.uk" target="_blank">www.scotlandspeople.gov.uk</a> )  Births after 1911 show the mother’s maiden names and spouses name next to the person they married which makes working out relationships easier.  Prior to 1911 these are not shown.  However, for births help is at hand because the General Registry Office for England and Wales (GRO) has created an index for births recorded from when the register was opened in 1837 which does contain the mother’s maiden name which at the time of writing only goes up to 1915.  This can be found at <a href="http://www.gro.gov.uk" target="_blank">www.gro.gov.uk</a>  For Scottish heritage as mentioned before you need to visit Scotlands People website. </p>
    <p>The 1939 register is an invaluable resource for those relatives who were born over 100 years ago or died before 1990.  It was taken on 29 September 1939 to record the civilian population so that identity cards and ration books could be issued.  After the war it was used to set up the NHS and updated until 1990.  It recorded:-</p>
        <ul>
            <li>Full name and any changes e.g. through marriage(an enormous help when researching female relatives</li>
            <li>Date of Birth</li>
            <li>Address</li>
            <li>Occupation</li>
            <li>Whether or not they were in the ARP</li>
        </ul>
        <p>It continues to be updated on the submission of death certificates so the redacted entries are slowly being revealed which makes it well worth rechecking.  At the time of writing it can only be viewed by subscription at <a href="http://www.findmypast.co.uk" target="_blank">www.findmypast.co.uk</a> </p>
        <p>Once back to 1911 the census records can be viewed back to 1841 on several subscriptions sites e.g. <a href="http://www.ancestry.co.uk" target="_blank">www.ancestry.co.uk</a> , <a href="http://www.findmypast.co.uk" target="_blank">www.findmypast.co.uk</a> to name a few but transcriptions can also be viewed on free sites e.g. www.familysearch.org.</p>
            <p>Census records tell us about siblings, occupations, addresses etc.... Check out the library which lists when the censuses were taken and what questions were asked.  The library also contains links to other sites, some which are free and some which require a subscription.</p>


